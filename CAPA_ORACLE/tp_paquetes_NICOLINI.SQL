CREATE OR REPLACE PACKAGE CAPA.PAC_FN_CAPA IS
    FUNCTION FUN_SALUDAR (P_NOMBRE IN VARCHAR2) RETURN VARCHAR2;
    PROCEDURE PRC_SALUDAR (P_NOMBRE IN VARCHAR2, 
                           P_APELLIDO IN VARCHAR2,
                           P_ERROR OUT VARCHAR2, 
                           P_ERROR_ORA OUT VARCHAR2);
END;

CREATE OR REPLACE PACKAGE BODY PAC_FN_CAPA IS
    V_MENSAJE VARCHAR2(100):= 'Hola, como estas ';
    V_RESU VARCHAR2(100);
    FUNCTION FUN_SALUDAR (P_NOMBRE IN VARCHAR2) RETURN VARCHAR2 IS
        BEGIN
            V_RESU:= V_MENSAJE||P_NOMBRE;
            RETURN V_RESU;
        END;
    END;
    
    CREATE OR REPLACE PROCEDURE PRC_SALUDAR (P_NOMBRE IN VARCHAR2,P_APELLIDO IN VARCHAR2,P_ERROR OUT VARCHAR2,P_ERROR_ORA OUT VARCHAR2) IS
        BEGIN
            DBMS_OUTPUT.put_line(V_MENSAJE||P_NOMBRE||' '||P_APELLIDO);
        END;
    END;
END;
