SELECT LOC.C_POSTAL,
       LOC.D_DESCRIP
           AS NOMBRE_LOCALIDAD,
       (SELECT TG.D_DATO
          FROM TABLAS_GENERALES TG
         WHERE N_TABLA = 16 AND TG.C_DATO = LOC.C_PROVINCIA)
           AS NOMBRE_PROVINCIA,
       (SELECT DEPTO.D_DESCRIP
          FROM DEPARTAMENTOS DEPTO
         WHERE     DEPTO.C_DEPARTAMENTO = LOC.C_DEPARTAMENTO
               AND DEPTO.C_PROVINCIA = LOC.C_PROVINCIA)
           AS DEPARTAMENTO,
       D_DESCRIP
           AS NOMBRE_LOCALIDAD
  FROM LOCALIDADES LOC
 WHERE D_DESCRIP LIKE 'COLONIA%' AND LOC.C_POSTAL BETWEEN 3350 AND 3400;

