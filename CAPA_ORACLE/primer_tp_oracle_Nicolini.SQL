SELECT PR.NOMBRE                       AS NOMBRE_PRODUCTO,
       PR.PRECIO                       AS PRECIO_PRODUCTO,
       (SELECT CATEGORIA
          FROM CATEGORIAS
         WHERE ID = PR.IDCATEGORIA)    AS CATEGORIA,
       (SELECT NOMBREMARCA
          FROM MARCAS
         WHERE ID = PR.IDMARCA)        AS MARCA
  FROM PRODUCTOS PR;

